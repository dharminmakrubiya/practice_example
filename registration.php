<h6>&nbsp; Steps on taking photos and video:</h6>

<ol>
	<li>Write down the following on a piece of paper&nbsp;:
	<ul>
		<li>Unique Code (See the top of this page)</li>
		<li>BTC Season 5</li>
		<li>Initial Weight (in lbs.)</li>
		<li>Date (MM/DD/YYY)</li>
	</ul>
	</li>
	<li>Take a photo showing you with the filled-up paper. (3 photos: Front, Side, Back)</li>
	<li>Take an 8-second video while weighing yourself.</li>
</ol>

